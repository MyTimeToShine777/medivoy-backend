'use strict';

import { AuditLog } from '../models/index.js';

export class AuditService {
    // â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
    // LOG AUTH EVENT
    // â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•

    async logAuthEvent(userId, action, details, ipAddress, userAgent) {
        try {
            const auditLog = await AuditLog.create({
                userId: userId || null,
                action,
                details: JSON.stringify(details),
                ipAddress,
                userAgent,
                timestamp: new Date()
            });

            console.log(`ğŸ“ Audit logged: ${action} by user ${userId}`);

            return auditLog;
        } catch (error) {
            console.error('âŒ Audit log error:', error.message);
        }
    }

    // â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
    // GET USER AUDIT LOGS
    // â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•

    async getUserAuditLogs(userId, limit = 50) {
        try {
            const logs = await AuditLog.findAll({
                where: { userId },
                order: [
                    ['timestamp', 'DESC']
                ],
                limit
            });

            return logs;
        } catch (error) {
            console.error('âŒ Get audit logs error:', error.message);
            throw error;
        }
    }

    // â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
    // GET ACTION HISTORY
    // â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•

    async getActionHistory(action, limit = 100) {
        try {
            const logs = await AuditLog.findAll({
                where: { action },
                order: [
                    ['timestamp', 'DESC']
                ],
                limit
            });

            return logs;
        } catch (error) {
            console.error('âŒ Get action history error:', error.message);
            throw error;
        }
    }
}

export const auditService = new AuditService();
export default auditService;